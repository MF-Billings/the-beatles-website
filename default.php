	<h2>Why do The Beatles matter?</h2>
	
	<p>
	<strong>The Beatles</strong> were vanguards of 1960s music and
	they&#39;ve left practically innumerable influences on it as a
	result. One of their most commonly recognized achievements is
	spearheading the 
	<em>British Invasion</em>, leading to the introduction to artists
	like the Rolling Stones, the Dave Clark Five, the Animals, Tom Jones,
	the Yardbirds (whose guitarist Jimmy Page would later form Led
	Zeppelin), and the Kinks. They permanently changed the way the music
	industry functioned, pioneering the modern music video, creating the
	concept album, and being the first band to play a stadium
	concert.</p>
	
	<p>Beyond the music itself, there&#39;s a wealth of interesting
	history created by <strong>The Beatles</strong> over their career. They&#39;ve been
	credited with popularizing the &quot;devil horns&quot; hand gesture -
	something that&#39;s become ubiquitous at rock concerts across the
	world with the album art for 
	<em>Yellow Submarine</em>, in which John Lennon&#39;s character is
	doing the gesture. The Vatican&#39;s official publication named 
	<em>Revolver</em> as the best pop album of all time (after they were 
	<a href="http://www.theguardian.com/music/2010/apr/12/vatican-beatles-john-lennon">
	forgiven</a> for John Lennon&#39;s infamous &quot;...bigger than
	Jesus&quot; comments).</p>
	
	<p>Numerous books have been written about The Beatles, and learning
